    def _get_latest_version(self, versions: List[str]) -> Optional[str]:
        """Get the latest version from a list of version strings"""
        if not versions:
            return None
        
        latest = versions[0]
        for v in versions[1:]:
            if compare_versions(v, latest) > 0:  # ADD THIS
                latest = v
        
        return latest
    
