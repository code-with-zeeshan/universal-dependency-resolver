    def _deduplicate_packages(self, packages: List[PackageInfo]) -> List[PackageInfo]:
        """Remove duplicate packages, keeping the latest version"""
        package_map = {}
        
        for pkg in packages:
            key = (pkg.name, pkg.ecosystem)
            if key not in package_map:
                package_map[key] = pkg
            else:
                # Compare versions and keep the newer one
                existing_pkg = package_map[key]
                if compare_versions(pkg.version, existing_pkg.version) > 0:  # ADD THIS
                    package_map[key] = pkg
                    logger.info(f"Deduplicating {pkg.name}: keeping {pkg.version} over {existing_pkg.version}")
        
        return list(package_map.values())
    
